public class Komentar {
    public static void main(String[] args) {
        System.out.println(sum(10,10));
    }

    /**
     * Menghitung jumlah a + b
     *
     * @param a nilai a
     * @param b nilai b
     * @return a + b
     */
    static int sum(int a, int b) {
        // jumlahkan a + b
        return a + b;
    }
}
